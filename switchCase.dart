void main(){
  int day = 0 ;
  switch(day){
    case 1:
      print("IT IS A MONDAY");
      break;
    case 2:
      print("IT IS A TUESDAY");
      break;
    case 3:
      print("IT IS A WEDNESDAY");
      break;
    case 4:
      print("IT IS A THURSDAY");
      break;
    case 5:
      print("IT IS A FRIDAY");
      break;
    case 6:
      print("IT IS A SATURDAY");
      break;
    case 7:
      print("IT IS A SUNDAY");
      break;
    default:
      print("NO DAY LIKE THAT");
  }
}